public class VariableIntro {
    public static void main(String[] args) {
        // declaration
        int a;
        float b;
        boolean holder;
        double poa;

        // Definition
        a = 10;
        b = 3.14f;
        holder = true;
        poa = 69.69;
        System.out.println("Value of a: " + a);
        System.out.println("Value of b: " + b);
        System.out.println("Value of b: " + b);
        System.out.println("Value of holder: " + holder);
        System.out.println("Value of poa: " + poa + "\n\n\n\n");

        // Initialization and declaration
        int c = 20;
        float d = 4.5f;
        boolean isTrue = false;
        double e = 69.69;
        System.out.println("Value of c: " + c);
        System.out.println("Value of d: " + d);
        System.out.println("Value of d: " + d);
        System.out.println("Value of isTrue: " + isTrue);
        System.out.println("Value of e: " + e + "\n\n\n");

        // multi declaration
        int f, g, h;
        f = 30;
        g = 40;
        h = 50;
        System.out.println("Value of f: " + f);
        System.out.println("Value of g: " + g);
        System.out.println("Value of h: " + h + "\n\n\n");
        String ha = "Ram";
        System.out.println(ha);
        char ram = '2';
        System.out.println(ram);
        String var1 = "Ramu is a good girl";
        System.out.println(var1);

    }

}
