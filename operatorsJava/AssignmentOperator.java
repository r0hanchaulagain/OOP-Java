public class AssignmentOperator {
    public static void main(String[] args) {
        int x = 10;
        x += 5; // x = x + 5
        System.out.println("x = " + x); // x = 15

        x -= 3; // x = x - 3
        System.out.println("x = " + x); // x = 12

        x *= 2; // x = x * 2
        System.out.println("x = " + x); // x = 24

        x /= 4; // x = x / 4
        System.out.println("x = " + x); // x = 6

        x %= 3; // x = x % 3
        System.out.println("x = " + x); // x = 0

    }
}
